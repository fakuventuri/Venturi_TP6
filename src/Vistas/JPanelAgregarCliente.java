    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (!comprobarCamposRellenos()) {
            jLabelResultado.setText("Complete los campos.");
            jLabelResultado.setVisible(true);
            return;
        }
        
        long telefono;
        
        try {
            telefono = Long.parseLong(txtTelefono.getText());
        } catch (NumberFormatException e) {
            jLabelResultado.setText("Telefono invalido. Solo numeros.");
            jLabelResultado.setVisible(true);
            return;
        }
        
        long dni;
        
        try {
            dni = Long.parseLong(txtDni.getText());
        } catch (NumberFormatException e) {
            jLabelResultado.setText("DNI invalido. Solo numeros.");
            jLabelResultado.setVisible(true);
            return;
        }
        
        
        Cliente nuevo = new Cliente(dni, txtNombre.getText(), txtApellido.getText(), txtCiudad.getText(), txtDireccion.getText());
        
        if (principal.getDirTel().agregarCliente(telefono, nuevo)) {
            jLabelResultado.setText("Cliente Agregado con exito!!");
            jLabelResultado.setVisible(true);
        } else {
            jLabelResultado.setText("El telefono ya existe!");
            jLabelResultado.setVisible(true);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

