    private boolean comprobarCamposRellenos(){
        if (txtApellido.getText().length()==0) {
            return false;
        }
        return txtApellido.getText().length() != 0 && txtCiudad.getText().length() != 0 && txtDireccion.getText().length() != 0 && txtDni.getText().length() != 0 && txtNombre.getText().length() != 0 && txtTelefono.getText().length() != 0;
    }

